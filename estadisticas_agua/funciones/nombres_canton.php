<?php

function nombre_canton($id) {
    $canton = array();
    if ($id == 100){
        $canton = "Alausí";
    }
    if ($id == 101){
        $canton = "Chambo";
    }
    if ($id == 102){
        $canton = "Chunchi";
    }
    if ($id == 103){
        $canton = "Colta";
    }
    if ($id == 104){
        $canton = "Cumandá";
    }
    if ($id == 105){
        $canton = "Guamote";
    }
    if ($id == 106){
        $canton = "Guano";
    }
    if ($id == 107){
        $canton = "Pallatanga";
    }
    if ($id == 108){
        $canton = "Penipe";
    }
    if ($id == 109){
        $canton = "Riobamba";
    }
    
    return $canton;
        }




